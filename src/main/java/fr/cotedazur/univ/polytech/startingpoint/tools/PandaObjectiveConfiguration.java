package fr.cotedazur.univ.polytech.startingpoint.tools;

/**
 * @class PandaObjectiveConfiguration de type enum
 * Types de configuration pour les objectifs de panda :
 *
 * Le joueur doit posséder sur son plateau individuel les sections
 * de bambou indiquées sur chaque carte
 */

public enum PandaObjectiveConfiguration {
    /** 2 sections de bambou jaune */
    TWO_YELLOW,
    /** 2 sections de bambou vert */
    TWO_GREEN,

    /** 2 sections de bambou rose */
    TWO_PINK,

    /** 3 sections de bambou vert */
    THREE_GREEN,

    /** 3 sections, une de chaque couleur*/
    ONE_OF_EACH,

}
